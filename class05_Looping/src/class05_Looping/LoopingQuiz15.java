package class05_Looping;

public class LoopingQuiz15 {

	public static void main(String[] args) {
		int number[] = new int[10];
		
		number [0]= 4;
		number [1]= 4;
		number [2]= 6;
		number [3]= 7;
		number [4]= 8;
		number [5]= 5;
		number [6]= 4;
		number [7]= 3;
		number [8]= 3;
		number [9] = 4;
		
		
		
		for (int i=0; i<=10; i++) {
			System.out.println(i);
		}
	
	}//main

}//class
